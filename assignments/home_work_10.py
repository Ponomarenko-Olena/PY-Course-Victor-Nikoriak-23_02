# Що станеться, якщо замість IndexError функція oops буде генерувати виняток KeyError?

def oops():
    # Явно викликаємо помилку індексу
    raise IndexError("Це помилка індексу!")

def caller():
    try:
        oops()
    except IndexError as e:
        print(f"Перехоплено виключення: {e}")

caller()

# Якщо ви зміните тип помилки в oops, але залишите except IndexError у caller, програма аварійно
# завершиться з помилкою KeyError.
# Блок except шукає збіг за типом. Оскільки KeyError не є IndexError, виключення не буде
# перехоплено в caller і "полетить" вище за стеком викликів, доки не зупинить усю програму.

# Task 2
# Напишіть функцію, яка приймає від користувача два числа за допомогою input(), називає їх a
# та b, а потім повертає значення a, піднесене до квадрата, поділене на b. Створіть блок
# try-except, який перехоплює виняток, якщо два значення, надані функцією input(), не є числами,
# а також якщо значення b дорівнює нулю (не можна ділити на нуль).

def calculate_power_division():
    try:
# Отримуємо дані від користувача
        a = input("Введіть число a: ")
        b = input("Введіть число b: ")

# Перетворюємо на числа (може виникнути ValueError)
        num_a = float(a)
        num_b = float(b)

# Обчислюємо квадрат a, поділений на b (може виникнути ZeroDivisionError)
        result = (num_a ** 2) / num_b
        return result

    except ValueError:
        return "Помилка: Ви ввели не число. Будь ласка, використовуйте цифри."

    except ZeroDivisionError:
        return "Помилка: На нуль ділити не можна (значення b не має бути 0)."


# Виклик функції та друк результату
print(calculate_power_division())
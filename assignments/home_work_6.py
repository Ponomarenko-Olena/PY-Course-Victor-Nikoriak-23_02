# Виокремлення чисел.
# Створіть список, що містить усі цілі числа від 1 до 100, потім знайдіть у цьому списку всі
# цілі числа, які діляться на 7, але не є кратними 5, і збережіть їх в окремому списку.
# Насамкінець виведіть цей список на екран.
# Умова: для ітерації використовуйте лише цикл while

# Крок 1: Створюємо список усіх цілих чисел від 1 до 100
all_numbers = []
i = 1
while i <= 100:
    all_numbers.append(i)
    i += 1

# Крок 2: Знаходимо числа, що діляться на 7, але не кратні 5
result_list = []
j = 0
while j < len(all_numbers):
    number = all_numbers[j]
    # Перевірка умови: ділиться на 7 (%) та НЕ ділиться на 5 (!= 0)
    if number % 7 == 0 and number % 5 != 0:
        result_list.append(number)
    j += 1

# Виводимо результат
print(result_list)
# Task 1
# Найбільше число
# Напишіть програму на Python, яка визначає найбільше число зі списку випадкових чисел
# довжиною 10
# Обмеження: для генерації чисел використовуйте лише цикл while та модуль random
import random

# Створюємо порожній список та лічильник
numbers = []
i = 0

# Генеруємо 10 випадкових чисел за допомогою циклу while
while i < 10:  #додає по одному випадковому числу в список, поки його довжина не досягне 10.
    numbers.append(random.randint(1, 100))  # Діапазон чисел від 1 до 100
    i += 1

print(f"Список чисел: {numbers}")

# Шукаємо найбільше число
# Припускаємо, що перший елемент найбільший
largest = numbers[0]
j = 1

# ми беремо перше число за "зразок" і в другому циклі порівнюємо його з кожним наступним.
# Якщо знаходимо більше — оновлюємо змінну largest
while j < len(numbers): # гарантує, що ми перевіримо всі елементи списку
    if numbers[j] > largest:
        largest = numbers[j]
    j += 1

print(f"Найбільше число у списку: {largest}")

# Task 2
# Унікальні спільні числа.
# Створіть 2 списки довжиною 10, що містять випадкові цілі числа від 1 до 10,
# а також третій список, що містить спільні цілі числа з цих двох початкових списків без дублікатів.
# Обмеження: для генерації чисел використовуйте лише цикл while та модуль random
import random

list1 = []
list2 = []
common_list = []

# Крок 1: Генеруємо 2 списки довжиною 10
i = 0
while i < 10: #Цикл виконується 10 разів, додаючи випадкові числа від 1 до 10 в обидва списки одночасно.
    list1.append(random.randint(1, 10))
    list2.append(random.randint(1, 10))
    i += 1

print(f"Список 1: {list1}")
print(f"Список 2: {list2}")

# Крок 2: Знаходимо спільні числа без дублікатів
j = 0
while j < len(list1): #Ми перебираємо list1. Для кожного числа перевіряємо дві умови:
    number = list1[j] #Чи є це число у другому списку?
    # Перевіряємо, чи є число в другому списку ТА чи ми його ще не додавали в спільний
    if number in list2 and number not in common_list:
        common_list.append(number)
    j += 1

print(f"Спільні числа (без дублікатів): {common_list}")

